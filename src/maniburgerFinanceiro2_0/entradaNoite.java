    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Double totalNoite=0.00;
        Double totalCartao = 0.00;
        Double totalDinheiro = 0.00;
        LancheDAO l = new LancheDAO();
        try {
            totalNoite=l.readEntrada(diaMes.getText());
            totalCartao = l.readEntradaCartao(diaMes.getText());
            totalDinheiro=l.readEntradaDinheiro(diaMes.getText());
        } catch (SQLException ex) {
            Logger.getLogger(entradaNoite.class.getName()).log(Level.SEVERE, null, ex);
        }
        entradaTotal.setText(Double.toString(totalNoite));
        entradaTotalCartao.setText(Double.toString(totalCartao));
        emtradaTotalDinheiro.setText(Double.toString(totalDinheiro));
        
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        jTable1.setRowSorter(new TableRowSorter(modelo));
        readJTable();
    }//GEN-LAST:event_jButton1ActionPerformed

