        public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        IngredientesDAO idao = new IngredientesDAO(); 
        Lanche_IngredienteDAO lidao= new Lanche_IngredienteDAO();
        for (Ingredientes i: idao.read()){
            i.setVendidos(lidao.readNumVendas(diaMes.getText(), i.getCod()));
            Double valorTotal= i.getVendidos()*i.getPreVendaPorcao();
            modelo.addRow(new Object []{
                i.getNome(),
                i.getCod(),
                i.getPreVendaPorcao(),
                i.getVendidos(),
                valorTotal
                
                
                
            });
            
        }
    }

