    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(Double.parseDouble(txtValor.getText())>=0){
        entradaDAO ent = new entradaDAO();
        Entrada e = new Entrada();
        if (txtNome.getText() != null && txtValor.getText() != null) {
            e.setNome(txtNome.getText());
            e.setValor(Double.parseDouble(txtValor.getText()));
            if (jRadioButton1.isSelected()) {
                e.setFormato(1);
                ent.updateCartaoEntrada(Double.parseDouble(txtValor.getText()));
            } else if (jRadioButton2.isSelected()) {
                e.setFormato(2);
                ent.updateDinheiroEntrada(Double.parseDouble(txtValor.getText()));
            } else {
                JOptionPane.showMessageDialog(null, "Selecione o formato da entrada ");
            }
        }
        
        if (e.getNome()!= null && e.getData()!=null && e.getValor()!=null && e.getFormato()== 1 ) {
            ent.create(e);
        }else if (e.getNome()!= null && e.getData()!=null && e.getValor()!=null && e.getFormato()== 2 ){
            ent.create(e);
        }else{
            JOptionPane.showMessageDialog(null, "Algum campo esta em branco ");
        }
        txtNome.setText(null);
        txtValor.setText(null);
        }else{
            JOptionPane.showMessageDialog(null, "Por favor digite um valor positivo!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


