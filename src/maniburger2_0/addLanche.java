    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        l.setDonoLanche(txtDonoLanche.getText());
        l.setIngredientes(t.listaIng(txtCodigoLanche.getText()));
        l.setTaxaEntrega(Double.parseDouble(txtTaxaEntrega.getText()));
        l.setTaxaExtra(txtTaxaExtra.getText());
        l.setValorTaxaExtra(Double.parseDouble(txtValorTaxaExtra.getText()));
        l.setPreco(t.calculaPreco(l));
        if (jRadioButton1.isSelected()) {
            l.setFormaPg(1);
        } else if (jRadioButton2.isSelected()) {
            l.setFormaPg(2);
            l.setTroco(t.calculaTroco(Double.parseDouble(jTextField3.getText()), l));
        } else {
            JOptionPane.showMessageDialog(null, "Selecione a forma de pagamento ");
        }
        txtLanche.setText(t.txtResumo(l));
        //MANDAR IMPRIMIR A STRING ABAIXO

        jButton3.setBackground(Color.yellow);
        //txtLanche.setText(tradutor(jTFCodigo.getText()));

    }//GEN-LAST:event_jButton3ActionPerformed

